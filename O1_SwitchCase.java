public class O1_SwitchCase {
  public static void main(String[] args) {

    int n=8;
    
    switch(n){
      case 1:
        System.out.println("monday");
        break;

      case 2:
        System.out.println("tue");
        break;

      case 3:
        System.out.println("wed");
        break;

      default:
        System.out.println("Holiday");
        break;
    }
  }
}
